  <div class="news-post clearfix">
      <h2>There Are Many Horse Artists - But How Many<br>Artist Horses??</h2>
      <h3>One! And I painted with Him!</h3>

      <div class="news-text">
        <p>How amazing was this? So many artists paint the image of the horse - but how many get to actually paint With a horse???</p>
        <p>I did - during Belmont week in Rocky Ridge MD where I visited Metro Meteor - an ex-racehorse that used to be a winning sprinter - and is now a winning artist - his work being sold in a local gallery as well as having his images used for pillows, totes, saddle pads, halters, etc. A generous portion of the proceeds goes directly to New Vocations Horse Rescue. I teamed up with Metro when I saw his work, thinking - I use a lot of my art to support causes I believe in - by donating directly or by Painting Live at events, fundraisers, and Private Client Galas - we need to work together!!</p>
        <p>It was one of the most singularly fun and enjoyable experiences I have had - Metro is truly a detailed artist and deeply aware of what he is doing See the video of us working together here on my site on the "Press" page, and google Metro to see where you can buy his art - and our collaborative pieces - as prints and support this work for other horses! Go, Metro, I love you!</p>
      
      </div>

