      <h2>Collaboration - Always a Winner</h2>

      <div class="news-text">
        <p>When the owner of <a href="http://www.morsewines.com/" target="_blank">Morse Wines Il Gioeillo Winery</a> reached out to me in appreciation of my art, particularly the piece "Into the Light", he indicated he was creating a new vintage of wines, "HorsePlay." He wanted to use the image for the label, and, by the way, as he and family are horse lovers, he'd love to support a horse cause with a percentage of the sales proceeds..... Was there any in particular that was close to my heart? A match made in heaven!</p>
        <p>The painting had been inspired by my support of the issues of the American Mustang, representing their movement from the darkness of persecution into the light of perennial freedom. I suggested we work with <a href="http://www.returntofreedom.org/" target="_blank">ReturntoFreedom.org</a>, an organization I had gotten to know while working with a local class of students about these issues.</p>
        <p>The label was created, the wine was born, a formal launch of a HorsePlay white wine is in the works, the red vintage won Gold at the Amador Fair Wine Competition, the <a href="https://www.facebook.com/HorsePlayWineClub" target="_blank">Horse PlayWine Club</a> has been born, and it is available for <a href="http://shop.morsewines.com/product.sc?productId=51&categoryId=1" target="_blank">purchase online</a>.</p>
        <p>Support the American Mustang, and remember to drink responsibly.</p>
